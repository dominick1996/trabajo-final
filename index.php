Sacsayhuamán (en quechua Saqsaywaman, de saqsay, lugar de saciarse, y waman, halcón, es decir, "Lugar donde se sacia el halcón") es una "fortaleza ceremonial" inca ubicada dos kilómetros al norte de la ciudad del Cuzco. Se comenzó a construir durante el gobierno de Pachacútec, en el siglo XV; sin embargo, fue Huayna Cápac quien le dio el toque final en el siglo XVI. Con el aniquilamiento de la nobleza inca desaparecieron de la memoria humana las técnicas que permitieron la construcción de esta monumental fortaleza o santuario; el cual produjo la admiración de Pizarro y sus hombres. Pedro Sancho, secretario de Pizarro, deja una primera descripción del edificio; pero la más detallada es la del Inca Garcilaso de la Vega (Cronista mestizo). La "fortaleza ceremonial" de Sacsayhuamán es con sus muros megalíticos, la mayor obra arquitectónica que realizaron los incas durante su apogeo. Desde la fortaleza se observa una singular vista panorámica de los entornos, incluyendo la ciudad del Cuzco.La construcción es típica de la arquitectura inca, aunque es el lugar donde esta técnica se usó a mayor escala. La roca se cortaba en la cantera de forma tosca con la forma aproximada requerida. Luego era arrastrada con cuerdas por cientos de hombres hasta el lugar de construcción, donde se le daba el tallado definitivo. Las cuerdas usadas eran tan impresionantes que fueron mencionadas por el conquistador español Diego de Trujillo en 1571 tras inspeccionar una habitación llena de material de construcción.


